#π的计算
#圆周率π是一个无理数，没有任何一个精确公式能够计算π值，π的计算只能采用近似算法。
#国际公认的PI值计算采用蒙特卡洛方法。
#蒙特卡洛（Monte Carlo）方法，又称随机抽样或统计试验方法。当所求解问题是某种事件出现的概率，或某随机变量期望值时，可以通过某种“试验”的方法求解。
#简单说，蒙特卡洛是利用随机试验求解问题的方法。

#应用蒙特卡洛方法求解π的步骤是这样的：
#1）首先构造一个单位正方形和1/4圆（圆的半径=正方形边长）
#2）随机向单位正方形和圆结构抛洒大量点。



